# ── VALIDATION ────────────────────────────────────────────────────────────────

VALID_DIFFICULTIES = {"simple", "moderate", "complex", "expert"}
VALID_BEHAVIORS = {"command", "explanation_only", "refuse", "clarify"}
VALID_SAFETY = {"safe", "soft_risky", "hard_blocked"}


def validate(corpus: list[dict]) -> None:
    """Validate corpus integrity before writing."""
    ids = [c["case_id"] for c in corpus]
    dupes = {x for x in ids if ids.count(x) > 1}
    if dupes:
        raise ValueError(f"Duplicate case_ids: {dupes}")

    for c in corpus:
        cid = c["case_id"]
        assert c["difficulty"] in VALID_DIFFICULTIES, f"{cid}: bad difficulty {c['difficulty']}"
        assert c["expected_behavior"] in VALID_BEHAVIORS, (
            f"{cid}: bad behavior {c['expected_behavior']}"
        )
        assert c["safety_level"] in VALID_SAFETY, f"{cid}: bad safety {c['safety_level']}"

        if c["safety_level"] == "hard_blocked":
            assert c["expected_command"] is None, (
                f"{cid}: hard_blocked must have expected_command=null"
            )
            assert c["expected_behavior"] == "refuse", (
                f"{cid}: hard_blocked must have expected_behavior=refuse"
            )


def main() -> None:
    validate(cases)

    out_path = Path(__file__).parent / "corpus.json"
    out_path.write_text(json.dumps(cases, indent=2, ensure_ascii=False) + "\n")
    print(f"Wrote {len(cases)} cases to {out_path}")

    from collections import Counter
    cats = Counter(c["category"] for c in cases)
    for cat, count in sorted(cats.items()):
        print(f"  {cat}: {count}")


if __name__ == "__main__":
    main()
